mt19937 rnd(chrono::steady_clock::now().time_since_epoch().count());

const int N = 1e6 + 1;
vector<ll> dp(N, 0);
// *-> KISS*
int solve() {
    ll n;
	cin >> n;
	dp[1] = 0; dp[2] = 1;
	for(ll i = 3; i < N; ++i) {
		dp[i] = (i - 1) * ((dp[i - 1] + dp[i - 2]) % MOD) % MOD;
	}
	cout << dp[n];
	return 0;
}
