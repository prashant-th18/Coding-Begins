int solve() {
    ll n, k; cin >> n >> k;
    ll sum = 1;
    vector<ll> v;
    v.push_back(0); v.push_back(1);
    vector<ll> prefix;
    prefix.push_back(0); prefix.push_back(1);
    for(ll j = k + 1; ; ++j)
    {
        ll temp = sum;
        if((j - k) >= k)
        {
            temp -= prefix[j - 2 * k];
        }
        sum += temp;
        if(temp > (ll)(1e9))
        {
            break;
        }
        else
        {
            v.push_back(temp);
            prefix.push_back(sum);
        }
    }
    vector<ll> orig;
    v.resize(unique(all(v)) - v.begin());
    auto f = y_combinator([&](auto f, int index, ll ssum) -> bool
    {
        if(index < 0) return false;
        if(ssum < 0) return false;
        if(ssum == 0) return true;
        if(v[index] > ssum)
        {
            return f(index - 1, ssum);
        }
        if(f(index - 1, ssum))
        {
            return true;
        }
        else if(f(index - 1, ssum - v[index]))
        {
            orig.push_back(v[index]);
            return true;
        }
        return false;
    });
    f(sz(v) - 1, n);
    orig.push_back(0);
    cout << sz(orig) << '\n';
    for(auto val : orig) cout << val << ' ';
    return 0;
}
