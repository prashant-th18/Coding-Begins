int solve() {
    int n; cin >> n;
    vector<int> v(n, 0);
    vector<bool> vis(n, false);
    unordered_map<int, bool> mp;
    vector<int> sq;
    sq.push_back(0); mp[0] = true;
    for(int i = 1; i <= 1e3; ++i) sq.push_back(i * i), mp[i * i] = true;
    for(int i = n - 1; i >= 0; --i) {
        int j = lower_bound(all(sq), i) - begin(sq);
        int elem = sq[j] - i;
        while(elem < n && !vis[elem]) {
            vis[elem] = true;
            v[i--] = elem;
            ++elem;
        }
        ++i;
    }
    for(int i = 0; i < n; ++i) {
        assert(mp[v[i] + i]);
    }
    for(auto& val : v) cout << val << ' ';
    return 0;
}
