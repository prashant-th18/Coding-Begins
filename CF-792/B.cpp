int solve() {
    ll a, b, c; cin >> a >> b >> c;
    ll x, y, z;
    // x > c
    ll s = 1, e = (c - a + b - 1) / b + 1;
    ll ans = 0;
    while(s <= e) {
        ll mid = (s + e) / 2;
        if(b * mid + a > c) {
            ans = mid;
            e = mid - 1;
        }
        else s = mid + 1;
    }
    x = b * ans + a;
    y = b;
    z = c;
    assert(x > c);
    assert((x % y) == a);
    assert((y % z) == b);
    assert((z % x) == c);
    cout << x << ' ' << y << ' ' << z;
    return 0;
}
