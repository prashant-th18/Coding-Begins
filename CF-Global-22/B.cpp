int solve() {
    ll n, k; cin >> n >> k;
	vector<ll> v(k);
	for(int i = 0; i < k; ++i) cin >> v[i];
	vector<ll> ans(n);
	ll how = n - k + 1;
	if(v[0] < 0) {
		ll q = v[0] / how;
		ll rem = abs(v[0]) % how;
		for(int i = 0; i < how; ++i) {
			ans[i] = q;
			if(rem > 0) {
				ans[i]--;
				rem--;
			}
		}
	}
	else {
		ll q = v[0] / how, rem = v[0] % how;
		for(int i = how - 1; i >= 0; --i) {
			ans[i] = q;
			if(rem > 0) {
				ans[i]++; rem--;
			}
		}
	}
	for(int i = how; i < n; ++i) {
		ll elem = v[i - how + 1];
		ans[i] = elem - v[i - how];
	}
	debug(ans);
	if(is_sorted(all(ans))) cout << "YES";
	else cout << "NO";
	return 0;
}
