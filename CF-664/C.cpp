int32_t main() {
    ios :: sync_with_stdio(false);
    cin.tie(nullptr);
    int n, m; cin >> n >> m;
    vector<int> a(n), b(m);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < m; i++) {
        cin >> b[i];
    }
    int or_res = 0;
    for (int i = 0; i < n; i++) {
        int mini = 1e9;
        for (int j = 0; j < m; j++) {
            mini = min(mini, a[i] & b[j]);
            debug(mini);
        }
        or_res |= mini;
    }
    cout << or_res;
    return 0;
}
