int solve() {
    int n; cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; i++) {
        cin >> v[i];
    }
    vector<ll> dp(32769, MOD);
    for(int i = 32768; i >= 0; --i)
    {
        if(i == 0 || i == 32768) dp[i] = 0;
        else
        {
            ll t = i, cnt = 0;
            while(t % 32768 != 0) 
            {
                dp[i] = min(dp[i], cnt + dp[t]);
                ++cnt;
                t *= 2;
                t = t % 32768;
            }
            dp[i] = min(dp[i], cnt);
            dp[i] = min(dp[i], 1 + dp[i + 1]);
            dp[i] = min(15LL, dp[i]);
        }
    }
    for (int i = 0; i < n; i++) {
        cout << dp[v[i]] << ' ';
    }
    return 0;
}
