int solve() {
    int n, k; cin >> n >> k;
    unordered_map<int, vector<int>, custom_hash> mp;
    for (int i = 0; i < n; i++) {
        int t; cin >> t;
        mp[t].push_back(i);
    }
    while(k--) {
        int a, b; cin >> a >> b;
        if(!mp.count(a) || !mp.count(b)) {
            cout << "No\n";
        }
        else {
            vector<int>& t1 = mp[a];
            vector<int>& t2 = mp[b];
            int in1 = t1[0], in2 = t2[sz(t2) - 1];
            if(in1 <= in2) {
                cout << "Yes\n";
            }
            else cout << "No\n";
        }
    }
    return 0;
}
